def get_audio_url(sura, aya, reciter="7"):  
    try:
        response = requests.get(f"https://api.alquran.cloud/v1/ayah/{sura}:{aya}/ar.alafasy")
        if response.status_code == 200:
            data = response.json()
            return data['data']['audio']
        else:
            return None
    except Exception:
        return None

